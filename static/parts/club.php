<h1>Club Alpbach Tirol</h1>


<div class="text">
	

<p>Wir, der <b>Club Alpbach Tirol</b>, sind eine der Initiativgruppen des <a href="http://www.alpbach.org/"><strong>Europäischen Forum Alpbach</strong>s</a>. In dieser Funktion sind wir an der inhaltlichen Gestaltung des Forums und der Organisation von Stipendien zur Teilnahme am Forum beteiligt.</p>
<p>Wir sind der Knotenpunkt zwischen engagierten Organisationen und motivierten Studierenden.</p>
<p>Mithilfe unserer Förderer und Partner können wir Studierenden, die in Tirol studieren oder aus Tirol stammen, eine Teilnahme am Europäischen Forum Alpbach ermöglichen. Die Teilnahme am Europäischen Forum Alpbach ist eine unvergessliche Erfahrung, die es jedem/jeder Studierenden ermöglicht, sich interdisziplinär weiterzuentwickeln und seinen/ihren Horizont zu erweitern.</p>


<h3>Wer sind wir, die Mitglieder des Club Alpbach Tirol?</h3>
<p>Die Mitglieder des Club Alpbach Tirols sind Studierende aller Fachrichtungen, Young Professionals und Ehrenmitglieder aus Wissenschaft, Wirtschaft, Politik und Gesellschaft – alle mit starkem Tirol-Bezug.




