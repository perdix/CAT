<h1>Europäisches Forum Alpbach</h1>

<div class="text">


<img src="public/img/efa_big.jpg" alt="efa" class="img-responsive" style="margin-bottom:15px; width:97%;">

<p>Seit 1945 findet jedes Jahr im August im Bergdorf Alpbach das „Europäische Forum Alpbach“ statt. Während knapp drei Wochen kommen ReferentInnen, ExpertInnen und TeilnehmerInnen aus allen Teilen der Welt in Alpbach zusammen, um Fragen der Zeit zu diskutieren und interdisziplinäre Lösungsansätze zu finden. Mittlerweile folgen mehr als 4.000 Menschen aus über 50 Staaten der Einladung am Europäischen Forum Alpbach teilzunehmen.</p>



<p>Das Programm des Europäischen Forums Alpbach gliedert sich in zwei Teile:</p>



<h3>Seminarwoche</h3>
Bis zu vierzehn Seminare sind verschiedenen wissenschaftlichen Disziplinen gewidmet. In jedem Seminar wird ein Themenschwerpunkt an sechs Halbtagen mit namhaften WissenschafterInnen diskutiert.</p>

<h3>Alpbacher Gespräche</h3>
<p>Diese zwei- bis dreitägigen Konferenzen befassen sich mit den Themen Architektur, Reform, Technologie, Politik, Gesundheit und Wirtschaft. Ergänzend dazu finden spezielle Workshops zu Bankwesen, Film und EU-Networking sowie ein Tiroltag und ein Universitätstag zu den Leistungen der Wissenschaft statt.</p>


Weitere Infos zum diesjährigen Programm findet man direkt unter <a href="http://www.alpbach.org/de/" title="">www.alpbach.org</a>.

